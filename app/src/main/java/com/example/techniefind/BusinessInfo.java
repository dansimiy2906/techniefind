package com.example.techniefind;

public class BusinessInfo {
    String Image,BusinessName,BusinessNumber,BusinessLocation,BusinessContact,BusinessBio,BusinessPk,Role;
    public BusinessInfo(){

    }

    public String getImage() {
        return Image;
    }

    public void setImage(String image) {
        Image = image;
    }

    public String getBusinessName() {
        return BusinessName;
    }

    public void setBusinessName(String businessName) {
        BusinessName = businessName;
    }

    public String getBusinessNumber() {
        return BusinessNumber;
    }

    public void setBusinessNumber(String businessNumber) {
        BusinessNumber = businessNumber;
    }

    public String getBusinessLocation() {
        return BusinessLocation;
    }

    public void setBusinessLocation(String businessLocation) {
        BusinessLocation = businessLocation;
    }

    public String getBusinessContact() {
        return BusinessContact;
    }

    public void setBusinessContact(String businessContact) {
        BusinessContact = businessContact;
    }

    public String getBusinessBio() {
        return BusinessBio;
    }

    public void setBusinessBio(String businessBio) {
        BusinessBio = businessBio;
    }

    public String getBusinessPk() {
        return BusinessPk;
    }

    public void setBusinessPk(String businessPk) {
        BusinessPk = businessPk;
    }

    public String getRole() {
        return Role;
    }

    public void setRole(String role) {
        Role = role;
    }
}
